/* ===== CONFETTI SHAPES (plus / square / circle + faint math symbols) ===== */
const CONFETTI_COLORS = ['#21a9ec', '#ffc23c', '#ff5b8d', '#2fd07f', '#6a8bff'];
const MATH_GLYPHS = ['π', '∑', '√', '∫', '×', '÷', '∞', 'f(x)', 'Δ', '²'];

function buildConfetti() {
  const container = document.getElementById('confetti');
  if (!container) return;
  container.innerHTML = '';

  for (let i = 0; i < 34; i++) {
    const span = document.createElement('span');
    const color = CONFETTI_COLORS[Math.floor(Math.random() * CONFETTI_COLORS.length)];
    const type = Math.random();

    if (type < 0.4) {
      // plus sign
      span.className = 'cf-plus';
      span.textContent = '+';
      span.style.fontSize = (14 + Math.random() * 22) + 'px';
      span.style.color = color;
    } else if (type < 0.7) {
      // square
      span.className = 'cf-square';
      const s = 7 + Math.random() * 12;
      span.style.width = s + 'px';
      span.style.height = s + 'px';
      span.style.background = color;
    } else if (type < 0.85) {
      // circle
      span.className = 'cf-circle';
      const s = 6 + Math.random() * 10;
      span.style.width = s + 'px';
      span.style.height = s + 'px';
      span.style.background = color;
    } else {
      // faint math glyph
      span.textContent = MATH_GLYPHS[Math.floor(Math.random() * MATH_GLYPHS.length)];
      span.style.fontFamily = "'Fredoka', sans-serif";
      span.style.fontSize = (18 + Math.random() * 22) + 'px';
      span.style.color = color;
      span.style.fontWeight = '600';
    }

    span.style.left = (Math.random() * 100) + '%';
    span.style.top = (Math.random() * 100) + '%';
    span.style.opacity = (0.35 + Math.random() * 0.4).toFixed(2);
    span.style.animationDuration = (6 + Math.random() * 10) + 's';
    span.style.animationDelay = (-Math.random() * 12) + 's';
    container.appendChild(span);
  }
}

/* ===== LANGUAGE ===== */
let currentLang = localStorage.getItem('lang') || 'uz';

function applyLang(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);
  const t = translations[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t[el.dataset.i18n] || '';
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = t[el.dataset.i18nHtml] || '';
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

document.querySelectorAll('.lang-btn').forEach(btn => {
  btn.addEventListener('click', () => applyLang(btn.dataset.lang));
});

/* ===== THEME (light is default) ===== */
const themeToggle = document.getElementById('themeToggle');
let isDark = localStorage.getItem('theme') === 'dark';

function applyTheme(dark) {
  isDark = dark;
  document.body.classList.toggle('dark', dark);
  themeToggle.innerHTML = dark
    ? '<i class="fas fa-sun"></i>'
    : '<i class="fas fa-moon"></i>';
  localStorage.setItem('theme', dark ? 'dark' : 'light');
  buildConfetti();
}

themeToggle.addEventListener('click', () => applyTheme(!isDark));

/* ===== CURSOR GLOW ===== */
const cursorGlow = document.getElementById('cursor-glow');

document.addEventListener('mousemove', e => {
  cursorGlow.style.left    = e.clientX + 'px';
  cursorGlow.style.top     = e.clientY + 'px';
  cursorGlow.style.opacity = '1';
});
document.addEventListener('mouseleave', () => { cursorGlow.style.opacity = '0'; });

/* ===== SCROLL FADE-IN ===== */
const observer = new IntersectionObserver(entries => {
  entries.forEach(e => { if (e.isIntersecting) e.target.classList.add('visible'); });
}, { threshold: 0.12 });

document.querySelectorAll('.fade-in').forEach(el => observer.observe(el));

/* ===== PDF MODAL ===== */
function openCert(filename) {
  const modal = document.getElementById('cert-modal');
  const frame = document.getElementById('cert-modal-frame');
  frame.src = filename + '#toolbar=0&navpanes=0&scrollbar=0';
  modal.style.display = 'flex';
  document.body.style.overflow = 'hidden';
}

function closeCertModal(force) {
  if (force === true || force?.target?.id === 'cert-modal') {
    document.getElementById('cert-modal').style.display = 'none';
    document.getElementById('cert-modal-frame').src = '';
    document.body.style.overflow = '';
  }
}

document.addEventListener('keydown', e => {
  if (e.key === 'Escape') closeCertModal(true);
});

/* ===== SCREENSHOT PROTECTION ===== */
function showSsWarning() {
  const w = document.getElementById('ss-warning');
  w.classList.add('show');
  setTimeout(() => w.classList.remove('show'), 2800);
}

// PrintScreen tugmasi bosilganda clipboardni tozalash + ogohlantirish
document.addEventListener('keyup', e => {
  if (e.key === 'PrintScreen') {
    navigator.clipboard.writeText('').catch(() => {});
    showSsWarning();
  }
});

document.addEventListener('keydown', e => {
  // Ctrl+P (chop etish) bloklash
  if ((e.ctrlKey || e.metaKey) && e.key === 'p') {
    e.preventDefault();
    showSsWarning();
  }
  // Mac skrinshot: Cmd+Shift+3/4/5
  if (e.metaKey && e.shiftKey && ['3','4','5','6'].includes(e.key)) {
    e.preventDefault();
    showSsWarning();
  }
});

// ===== CONTENT PROTECTION ===== //
document.addEventListener('contextmenu', e => {
  if (e.target.tagName === 'IMG' ||
      e.target.closest('#cert-modal') ||
      e.target.closest('.cert-modal-box')) {
    e.preventDefault();
  }
});

document.querySelectorAll('img').forEach(img => {
  img.draggable = false;
  img.addEventListener('dragstart', e => e.preventDefault());
});

